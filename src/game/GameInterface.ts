export interface Game {
  name: string; // The name of the game.
  image: string; // An image that represents the game.
  url: string; // The URL where the game can be played.
  backgroundImage: string; // The image used as the background of the game.
  header: string; // The text or image used as the header of the game.
  ad: string; // The advertisement displayed in the game.
  startButton: string; // The text or image used as the start button for the game.
  termsAndConditions: string; // The terms and conditions for playing the game.
}
